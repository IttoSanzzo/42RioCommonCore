#include "cube.h"

int	cb_rgb(int r, int g, int b)
{
	return (1 << 24 | r << 16 | g << 8 | b);
}

int	cb_argb(int r, int g, int b, int a)
{
	return (a << 24 | r << 16 | g << 8 | b);
}
